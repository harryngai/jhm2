# 類型、運算子和表達式

# 基本數據類型
# 整數 (int), 浮點數 (float), 布爾值 (bool), 字符串 (str)
x = 42        # 整數
y = 3.14      # 浮點數
is_true = True    # 布爾值
name = "Python"   # 字符串

# 運算符
# 算術運算符
a = 10 + 5   # 加法
b = 10 - 5   # 減法
c = 10 * 5   # 乘法
d = 10 / 5   # 除法
e = 10 % 3   # 取餘
f = 2 ** 3   # 冪運算

# 比較運算符
# 返回布爾值
x = 5 > 3    # 大於
y = 5 < 3    # 小於
z = 5 == 5   # 等於
w = 5 != 3   # 不等於

# 類型轉換
# 常用的類型轉換方法
str_num = "123"
num = int(str_num)    # 字符串轉整數
float_num = float("3.14")  # 字符串轉浮點數
str_val = str(42)     # 數字轉字符串

# 表達式示例
# 混合運算
age = 20
country = "TW"
result = (10 + 5) * 2    # 運算符優先級
is_valid = (age >= 18) and (country == "TW")  # 邏輯運算

# 重要提醒：
# 1. 除法運算始終返回浮點數
# 2. 整數除法使用 // 運算符
# 3. 類型轉換失敗會拋出 ValueError
# 4. 布爾值在數字環境中：True = 1, False = 0